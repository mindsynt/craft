# ── Slash command helper ──────────────────────────────────
BUILTIN_SLASH_COMMANDS: set[str] = {
    "init",
    "review",
    "dream",
    "distill",
    "goal",
    "deep-research",
}


def slash_command_description(
    t_func,
    name: str,
    fallback: str | None = None,
) -> str | None:
    """Get translated description for a built-in slash command."""
    if name not in BUILTIN_SLASH_COMMANDS:
        return fallback
    translated = t_func(f"tui.slash.{name}.description")
    return translated or fallback


# ── Skill helper functions ────────────────────────────────

def skill_description(
    t_func,
    name: str,
    fallback: str | None = None,
    bundled: bool = False,
) -> str | None:
    """Get translated description for a bundled skill."""
    if not bundled:
        return fallback
    key = f"tui.skill.{name}.description"
    val = t_func(key)
    return val if val != key else fallback


def skill_slash_aliases(
    t_func,
    name: str,
    bundled: bool = False,
) -> list[str]:
    """Get slash aliases for a bundled skill."""
    if not bundled:
        return []
    key = f"tui.skill.{name}.slash"
    val = t_func(key)
    if val == key:
        return []
    return [a.strip() for a in val.split("|") if a.strip()]


def resolve_skill_slash(
    t_func,
    alias: str,
    commands: list[dict],
) -> str | None:
    """Resolve a slash alias to a skill command name."""
    for cmd in commands:
        if cmd.get("bundled"):
            aliases = skill_slash_aliases(t_func, cmd["name"], True)
            if alias in aliases:
                return cmd["name"]
    return None


# ── I18n class ────────────────────────────────────────────

class I18n:
    """Internationalization manager."""

    def __init__(self, lang: str = "zh"):
        self.lang = lang
        self._strings = DICTS.get(lang, ZH)

    def t(self, key: str, *args, **kwargs) -> str:
        """Translate a key."""
        text = self._strings.get(key, key)
        if args:
            text = text % args
        elif kwargs:
            text = text % kwargs
        return text

    def set_lang(self, lang: str):
        """Change the current language."""
        self.lang = lang
        self._strings = DICTS.get(lang, ZH)

    @property
    def available_languages(self) -> list[str]:
        """Return list of available language codes."""
        return list(DICTS.keys())

i18n = I18n("zh")
